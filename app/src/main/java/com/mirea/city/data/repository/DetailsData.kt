fun getCityAndCountryByPlaceId(placeId: String): Pair<String, String> {
    return when {

        placeId.startsWith("red_square") || placeId.startsWith("moscow_") ->
            "Moscow" to "Russia"

        placeId.startsWith("hermitage") || placeId.startsWith("savior_") ||
                placeId.startsWith("peterhof") || placeId.startsWith("mariinsky") ->
            "Saint Petersburg" to "Russia"

        placeId.startsWith("statue_liberty") || placeId.startsWith("empire_") ||
                placeId.startsWith("times_square") || placeId.startsWith("metropolitan_") ->
            "New York" to "USA"

        placeId.startsWith("walk_of_fame") || placeId.startsWith("griffith_") ||
                placeId.startsWith("getty_") || placeId.startsWith("universal_") ->
            "Los Angeles" to "USA"

        placeId.startsWith("great_wall") || placeId.startsWith("forbidden_") ||
                placeId.startsWith("temple_of_heaven") || placeId.startsWith("birds_nest") ->
            "Beijing" to "China"

        placeId.startsWith("the_bund") || placeId.startsWith("shanghai_") ||
                placeId.startsWith("oriental_pearl") ->
            "Shanghai" to "China"

        placeId.startsWith("tower_of_london") || placeId.startsWith("buckingham_") ||
                placeId.startsWith("british_") || placeId.startsWith("london_") ->
            "London" to "UK"

        placeId.startsWith("royal_mile") || placeId.startsWith("holyrood_") ||
                placeId.startsWith("national_museum_scotland") || placeId.startsWith("calton_") ->
            "Edinburgh" to "UK"

        placeId.startsWith("batu_caves") || placeId.startsWith("kl_tower") ||
                placeId.startsWith("merdeka_") || placeId.startsWith("suria_") ->
            "Kuala Lumpur" to "Malaysia"

        placeId.startsWith("kek_lok_si") || placeId.startsWith("penang_") ||
                placeId.startsWith("clan_") || placeId.startsWith("blue_") ->
            "George Town" to "Malaysia"

        placeId.startsWith("vatican_") || placeId.startsWith("trevi_") ||
                placeId.startsWith("pantheon") || placeId.startsWith("spanish_") ->
            "Rome" to "Italy"

        placeId.startsWith("st_marks_") || placeId.startsWith("doges_") ||
                placeId.startsWith("bridge_of_sighs") ->
            "Venice" to "Italy"

        placeId.startsWith("dubai_") || placeId.startsWith("palm_") ->
            "Dubai" to "UAE"

        placeId.startsWith("sheikh_zayed_") || placeId.startsWith("louvre_abu_") ||
                placeId.startsWith("emirates_") || placeId.startsWith("yas_") ->
            "Abu Dhabi" to "UAE"

        placeId.startsWith("louvre") || placeId.startsWith("notre_dame") ||
                placeId.startsWith("arc_de_") || placeId.startsWith("champs_") ->
            "Paris" to "France"

        placeId.startsWith("vieux_") || placeId.startsWith("calanques") ||
                placeId.startsWith("chateau_") || placeId.startsWith("mucem") ->
            "Marseille" to "France"

        else -> "Unknown" to "Unknown"
    }
}